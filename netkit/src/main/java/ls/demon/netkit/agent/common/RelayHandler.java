package ls.demon.netkit.agent.common;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import io.netty.buffer.Unpooled;
import io.netty.channel.Channel;
import io.netty.channel.ChannelHandlerContext;
import io.netty.channel.ChannelInboundHandlerAdapter;
import io.netty.util.ReferenceCountUtil;
import ls.demon.netkit.util.PipeUtils;
import ls.demon.netkit.util.SocksServerUtils;

public final class RelayHandler extends ChannelInboundHandlerAdapter {
    /**
    * Logger for this class
    */
    private static final Logger logger = LoggerFactory.getLogger(RelayHandler.class);

    private final Channel       relayChannel;

    public RelayHandler(Channel relayChannel) {
        this.relayChannel = relayChannel;
    }

    @Override
    public void channelActive(ChannelHandlerContext ctx) {
        ctx.writeAndFlush(Unpooled.EMPTY_BUFFER);
    }

    @Override
    public void channelRead(ChannelHandlerContext ctx, Object msg) {
        logger.info("{}, {}", ctx, msg);
        PipeUtils.showAll("" + ctx.hashCode(), ctx.pipeline());
        if (relayChannel.isActive()) {
            relayChannel.writeAndFlush(msg);
        } else {
            ReferenceCountUtil.release(msg);
        }
    }

    @Override
    public void channelInactive(ChannelHandlerContext ctx) {
        logger.info("channelInactive");
        if (relayChannel.isActive()) {
            SocksServerUtils.closeOnFlush(relayChannel);
        }
    }

    @Override
    public void exceptionCaught(ChannelHandlerContext ctx, Throwable cause) {
        //        cause.printStackTrace();
        logger.error("", cause);
        ctx.close();
    }
}
